B. 自杀 / 自伤风险模块
C. 躁狂或轻躁狂相关模块
D. 惊恐发作相关模块
E. 广场恐惧相关模块
F. 社交焦虑相关模块
G. 强迫相关模块
H. 创伤及应激相关模块
I. 酒精使用相关模块
